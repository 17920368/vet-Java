package veterinaria;

/**
 *
 * @author Victor Rodriguez
 */
public class Animal {
    private String nombre;
    private String color;
    private int  edad;
    private double precio;

    public Animal(String nombre, String color, int edad, double precio) {
        this.nombre = nombre;
        this.color = color;
        this.edad = edad;
        this.precio = precio;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

}
